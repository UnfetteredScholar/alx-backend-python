#!/usr/bin/env python3
"""
Defines an annotated floor function
that returns the floor of a float
"""
import math


def floor(n: float) -> int:
    """
    Takes a float n as argument and returns the floor of the float

    Args:
        n (float): the number to be floored

    Returns:
        the floor of the float
    """
    return math.floor(n)
